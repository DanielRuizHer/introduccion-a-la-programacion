#include<iostream>

using namespace std;

int main(){
	int resultado;
	//suma(+)
	resultado = 4+5;
	cout<<"suma: "<<resultado<<endl;
	//resta(-)
	resultado =8-4;
	cout<<"resta: "<<resultado<<endl;
	//mult.(*)
	resultado =5*3;
	cout<<"mult.: "<<resultado<<endl;
	//div.(/)
	resultado = 5/3;
	cout<<"div.: "<<resultado<<endl;
	//mod.(%)
	resultado = 4%2;
	cout<<"mod.: "<<resultado<<endl;
	
	
	return 0;
}
